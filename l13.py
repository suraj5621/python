# standard print float
n=15.65
print('{:f}'.format(n))
print('{:8f}'.format(n))
print('{:8.3f}'.format(n))
print('{:+8.3f}'.format(n))
print('{:<8.2f}'.format(n))
print('{:*<8.2f}'.format(n))
print('{:>8.2f}'.format(n))
print('{:*>8.2f}'.format(n))
print('{:^8.2f}'.format(n))
print('{:*^8.2f}'.format(n))
